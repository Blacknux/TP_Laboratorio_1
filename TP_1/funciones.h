/**
*\brief Recibe los dos operandos y los suma
*/

int sumar(int x, int y)
{
    int suma;
    suma=x+y;
    printf("El resultado de la suma de %d y %d es: %d\n",x,y,suma);


}

/**
*\brief Recibe los dos operandos y los resta
*/

int restar(int x, int y)
{
    int resta;
    resta=x-y;
    printf("El resultado de la resta de %d y %d es: %d\n",x,y,resta);


}

/**
*\brief Recibe los dos operandos y los multiplica
*/


int multiplicar(int x, int y)
{
    int multiplica;
    multiplica=x*y;
    printf("El resultado de la multiplicacion de %d y %d es: %d\n",x,y,multiplica);


}
